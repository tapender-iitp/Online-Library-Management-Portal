          <p align=justify>The Central Library provides a healthThe Central Library of IIT Patna is one of the advanced libraries in terms of its collection and services. Central Library caters to the information needs of its highly demanding faculty members, students, research scholar as well as staff of the Institute, by offering a wide range of Information Technology (IT) based (and value added) services and products. The Central Library is in fast growing stage. It was started in December 2008 with approximately 1000 books and one online resource. During the short span of time the library has acquired more than 6000 books and about 25 online e-resources including e-books and e-journals. This library is equipped with modern technology of library automation system with Online Public Access Catalogue (OPAC) and Self Check-In and Shelf Check-Out (Drop Box) system using RFID technology. The Library has 15 computers for accessing the different e-resources of the library.

